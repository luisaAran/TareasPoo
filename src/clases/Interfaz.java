    private void calcularPromedioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularPromedioActionPerformed
              if (listaEstudiantes.isEmpty()) {
            jTextArea1.append("No hay estudiantes registrados.\n");
            return;
        }

        double sumaNotas = 0;
        for (int i = 0; i < listaEstudiantes.size(); i++) {
            sumaNotas += listaEstudiantes.get(i).getNota();
        }

        double promedio = sumaNotas / listaEstudiantes.size();
        jTextArea1.append("El promedio de notas es: " + promedio + "\n");
    
    }//GEN-LAST:event_calcularPromedioActionPerformed

