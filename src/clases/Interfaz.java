    private void agregarEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarEstudianteActionPerformed
       try {
            String nombre = jTextField1.getText();
            int edad = Integer.parseInt(jTextField2.getText());
            double nota = Double.parseDouble(jTextField3.getText());

            if (nombre.isEmpty()) {
                jTextArea1.append("El nombre no puede estar vacío.\n");
                return;
            }

            Estudiante nuevoEstudiante = new Estudiante(nombre, edad, nota);
            listaEstudiantes.add(nuevoEstudiante);
            jTextArea1.append("Estudiante agregado: \n" + "Nombre: " + nombre + "\nEdad: " + edad + "\nNota: " + nota + "\n");
        } catch (NumberFormatException e) {
            jTextArea1.append("Edad y nota deben ser valores numéricos válidos.\n");
        }
    }//GEN-LAST:event_agregarEstudianteActionPerformed

